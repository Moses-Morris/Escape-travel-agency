<h1>Essential Travel Documents & Expert Advice</h1>
<h1>✅ What International Clients Need to Travel to any country</h1>
    <h2>1. Passport</h2>
    <ul>
        <li>Ensure it’s valid for at least <strong>6 months beyond your travel date</strong>.</li>
        <li>Have at least <strong>2 blank pages</strong> for visa stamps.</li>
        <li>Store a <strong>photocopy or digital backup</strong> for safety.</li>
    </ul>

    <h2>2. Visa (eVisa)</h2>
    <ul>
        <li>Apply on official websites like <a href="https://evisa.go.ke">evisa.go.ke</a>.</li>
        <li>Print your visa confirmation and carry it with you.</li>
        <li>Check processing time and required documents (photo, bookings, etc.).</li>
    </ul>

    <h2>3. Yellow Fever Vaccination</h2>
    <ul>
        <li>Required for most African destinations – get vaccinated <strong>10 days before travel</strong>.</li>
        <li>Carry the <strong>original certificate</strong> at all times.</li>
        <li>Consider malaria prophylaxis for safaris or rural areas.</li>
    </ul>

    <h2>4. Return or Onward Ticket</h2>
    <ul>
        <li>Immigration may ask for proof of return or onward travel.</li>
        <li>Use refundable/flexible bookings if plans are uncertain.</li>
        <li>Always have a <strong>copy of your itinerary</strong>.</li>
    </ul>

    <h2>5. Travel Insurance</h2>
    <ul>
        <li>Highly recommended for <strong>medical emergencies and cancellations</strong>.</li>
        <li>Cover activities like safaris, hiking, etc.</li>
        <li>Print your policy and save emergency numbers.</li>
    </ul>

    <h2>6. Hotel / Travel Itinerary</h2>
    <ul>
        <li>Carry <strong>booking confirmations</strong> for accommodations.</li>
        <li>Prepare a simple itinerary (with dates and destinations).</li>
        <li>Keep digital copies accessible via email or phone.</li>
    </ul>

    <h2>7. Emergency Contact Info</h2>
    <ul>
        <li>List your <strong>embassy, family, and hotel contacts</strong>.</li>
        <li>Save contacts both on paper and on your phone.</li>
        <li>Enable emergency sharing features on your device.</li>
    </ul>

